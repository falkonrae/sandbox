#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;

	i = 0;
	if (src == 0)
	{
		return (0);
	}
	if (dstsize > 0)
	{
		while (src[i] != '\0' && i < dstsize - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = '\0';
	}
	if (i >= ft_strlen(src))
	{
		return (i);
	}
	else
	{
		return (ft_strlen(src));
	}
}
